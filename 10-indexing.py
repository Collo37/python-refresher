# indexing

names = ["Collins", "Derrick", "Oduor", "Collo", "Senpai"]
sentence = "This is just a random sentence"

print("Item at last index in the list:", names[len(names) - 1])
print("First letter of the sentence:", sentence[0])

# negative indexing
print("Third-last item in the list:", names[-3])
print("Second-last letter in the sentence:", sentence[-2])